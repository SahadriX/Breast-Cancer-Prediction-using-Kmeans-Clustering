import pandas as pd

df = pd.read_csv("/content/Breast_cancer_data(1).csv")

df.head()

from sklearn.preprocessing import StandardScaler

df_cleaned = df.drop(['id', 'diagnosis'], axis=1)

# Normalize the data
scaler = StandardScaler()
df_normalized = scaler.fit_transform(df_cleaned)

df_normalized = pd.DataFrame(df_normalized, columns=df_cleaned.columns)

# Display the first few rows of the normalized data
df_normalized.head()

import matplotlib.pyplot as plt
from sklearn.cluster import KMeans

kmeans = KMeans(n_clusters=2, random_state=0)
kmeans.fit(df_normalized)

df_normalized['Cluster'] = kmeans.labels_

from sklearn.decomposition import PCA

pca = PCA(n_components=2)
data_pca = pca.fit_transform(df_normalized.drop('Cluster', axis=1))

plt.figure(figsize=(10, 6))
plt.scatter(data_pca[:, 0], data_pca[:, 1], c=df_normalized['Cluster'], cmap='viridis', marker='o')
plt.xlabel('PCA Component 1')
plt.ylabel('PCA Component 2')
plt.title('K-Means Clustering of Breast Cancer Data')
plt.colorbar(label='Cluster')
plt.show()

